package jump.to.java.chap4;

public class Quest4 {

    /**
     * 문제 4번
     * for문을 사용해 1부터 100까지의 숫자를 출력해 보자.
     */
    public static void main(String[] args) {
        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
        }
    } // end of main
} // end of class
